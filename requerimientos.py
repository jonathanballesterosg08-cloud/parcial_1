# 1. Requerimientos Funcionales (lo que el programa debe hacer)
# 1.1. Mundo del juego

# El juego debe estar ambientado en un bosque encantado.

# Debe existir un jugador que recorra el bosque.

# Debe haber criaturas que habitan el bosque.

# 1.2. Criaturas

# Deben existir al menos dos tipos diferentes de criaturas.

# Algunas criaturas deben ser amigables.

# Algunas criaturas deben ser hostiles.

# Las criaturas deben tener características comunes, como nombre, energía, etc.

# Las criaturas deben poder interactuar con el jugador (amistosamente o atacando).

# 1.3. Jugador

# El jugador debe poder tomar decisiones durante el juego.

# Las decisiones del jugador deben afectar su progreso.

# El jugador debe poder interactuar con las criaturas.

# El jugador debe tener atributos como vida e inventario (o cualquier tipo de estado).

# 1.4. Interacción y mecánicas

#  El juego debe permitir que el jugador avance, huya o interactúe.

#  Las decisiones del jugador deben ser evaluadas mediante condicionales.

# La aparición de criaturas debe ser aleatoria.

# El recorrido del bosque debe manejarse usando ciclos (bucle de juego).

# El juego debe finalizar cuando el jugador pierda o termine su vida.

# 2. Requerimientos Técnicos (cómo debe construirse)
# 2.1. Programación orientada a objetos

# El juego debe estar construido con clases.

# Debe usarse herencia para representar las criaturas.

# Las criaturas hijas deben sobrescribir o extender el comportamiento de la clase padre.

#Se deben usar funciones (métodos) para realizar las acciones del juego.